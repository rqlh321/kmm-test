import com.example.kmm_test.Greeting
import com.example.kmm_test.SimpleItem
import kotlinx.coroutines.flow.launchIn
import kotlinx.coroutines.flow.onEach

class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            MyApplicationTheme {
                Surface(
                    modifier = Modifier.fillMaxSize(),
                    color = MaterialTheme.colorScheme.background
                ) {
                    val composableScope = rememberCoroutineScope()
                    var simpleItems by remember { mutableStateOf(emptyList<SimpleItem>()) }

                    LaunchedEffect(Unit) {
                        Greeting().greet()
                            .onEach { simpleItems = it }
                            .launchIn(composableScope)
                    }
                    LazyColumn(
                        contentPadding = PaddingValues(16.dp),
                        verticalArrangement = Arrangement.spacedBy(8.dp),
                    ) { items(simpleItems) { it.ui { println(it) } } }
                }
            }
        }
    }
}

@Composable
fun GreetingView(text: String) {
    Text(text = text)
}

